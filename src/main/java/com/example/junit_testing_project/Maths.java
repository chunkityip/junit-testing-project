package com.example.junit_testing_project;

public class Maths {

    public int add(int p1, int p2) {
        return p1 + p2;
    }

    public int sub(int p1, int p2) {
        return p1 - p2;
    }

    public int multiply(int p1 , int p2) {
        return p1 * p2;
    }

    public int divide(int p1 , int p2) {
        return p1 / p2;
    }


}